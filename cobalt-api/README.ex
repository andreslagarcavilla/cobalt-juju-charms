Overview
--------

This charm provides Cobalt (http://github.com/gridcentric/cobalt) from Gridcentric (http://www.gridcentric.com).

Usage
-----

Simply deploy the charm and add a relation to nova-api:

    juju deploy cobalt-api
    juju add-relation nova-api cobalt-api

